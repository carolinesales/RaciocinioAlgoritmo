#Escrever um algoritmo para calcular a média de 4 notas.

print("Bem-vindo(a) ao Sistema Acadêmico!")

nota01 = float(input("Informe a 1º nota: "))
nota02 = float(input("Informe a 2º nota: "))
nota03 = float(input("Informe a 3º nota: "))
nota04 = float(input("Informe a 4º nota: "))

calculoMediaNotas = (nota01 + nota02 + nota03 + nota04) / 4

print("A Média Final é: ", calculoMediaNotas)